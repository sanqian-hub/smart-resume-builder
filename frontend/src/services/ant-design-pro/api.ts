/** 用户上传头像 POST /api/file/upload-avatar */
export const uploadAvatar = (file: File) => {
  // 打包文件
  const formData = new FormData();
  formData.append('file', file);

  return request<string>('/api/file/upload-avatar', {
    method: 'POST',
    data: formData,
    requestType: 'form',
  });
};


